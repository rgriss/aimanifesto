<script setup>
import { Head, Link, router } from '@inertiajs/vue3';
import GuestLayout from '@/layouts/GuestLayout.vue';
import { PageHeader, SectionHeading, Card, Badge } from '@/components';
import { ref } from 'vue';

const props = defineProps({
    tools: Object,
    filters: {
        type: Object,
        default: () => ({})
    },
});

const search = ref(props.filters?.search || '');
const category = ref(props.filters?.category || '');
const pricing = ref(props.filters?.pricing || '');
const sort = ref(props.filters?.sort || 'name');

const applyFilters = () => {
    router.get('/tools', {
        search: search.value,
        category: category.value,
        pricing: pricing.value,
        sort: sort.value,
    }, {
        preserveState: true,
        preserveScroll: true,
    });
};
</script>

<template>
    <Head title="AI Tools Directory - AI Manifesto" />

    <GuestLayout>
        <div class="py-12">
            <div class="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
                <!-- Header -->
                <PageHeader
                    title="AI Tools Directory"
                    description="Curated AI tools tested and reviewed by Ryan Grissinger. Real insights from years of AI implementation experience."
                    :gradient="true"
                />

                <!-- Filters -->
                <Card class="mb-8">
                    <h3 class="text-lg font-bold text-foreground mb-4">Filter & Search</h3>
                    <div class="grid grid-cols-1 md:grid-cols-4 gap-4">
                        <!-- Search -->
                        <div>
                            <label class="block text-sm font-medium text-foreground mb-2">
                                Search
                            </label>
                            <input
                                v-model="search"
                                @input="applyFilters"
                                type="text"
                                placeholder="Search tools..."
                                class="w-full rounded-md border-border bg-background text-foreground px-3 py-2 focus:outline-none focus:ring-2 focus:ring-info"
                            />
                        </div>

                        <!-- Pricing Filter -->
                        <div>
                            <label class="block text-sm font-medium text-foreground mb-2">
                                Pricing
                            </label>
                            <select
                                v-model="pricing"
                                @change="applyFilters"
                                class="w-full rounded-md border-border bg-background text-foreground px-3 py-2 focus:outline-none focus:ring-2 focus:ring-info"
                            >
                                <option value="">All</option>
                                <option value="free">Free</option>
                                <option value="freemium">Freemium</option>
                                <option value="paid">Paid</option>
                                <option value="enterprise">Enterprise</option>
                            </select>
                        </div>

                        <!-- Sort -->
                        <div>
                            <label class="block text-sm font-medium text-foreground mb-2">
                                Sort By
                            </label>
                            <select
                                v-model="sort"
                                @change="applyFilters"
                                class="w-full rounded-md border-border bg-background text-foreground px-3 py-2 focus:outline-none focus:ring-2 focus:ring-info"
                            >
                                <option value="name">Name</option>
                                <option value="rating">Rating</option>
                                <option value="views">Most Viewed</option>
                                <option value="recent">Recently Added</option>
                            </select>
                        </div>

                        <!-- Clear Filters -->
                        <div class="flex items-end">
                            <Link
                                href="/tools"
                                class="w-full px-4 py-2 bg-secondary text-secondary-foreground rounded-md hover:bg-secondary/90 text-center font-semibold transition-colors"
                            >
                                Clear Filters
                            </Link>
                        </div>
                    </div>
                </Card>

                <!-- Tools Grid -->
                <div v-if="tools.data && tools.data.length > 0">
                    <div class="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6 mb-8">
                        <Link
                            v-for="tool in tools.data"
                            :key="tool.id"
                            :href="`/tools/${tool.slug}`"
                            class="group"
                        >
                            <Card>
                                <div class="flex items-start justify-between mb-3">
                                    <h3 class="text-xl font-bold text-foreground group-hover:text-info transition-colors">
                                        {{ tool.name }}
                                    </h3>
                                    <Badge v-if="tool.is_featured" variant="warning" size="sm">
                                        Featured
                                    </Badge>
                                </div>
                                <p class="text-muted-foreground mb-4 text-sm">
                                    {{ tool.description }}
                                </p>
                                <div class="flex items-center justify-between text-sm mb-2">
                                    <span class="text-muted-foreground">
                                        {{ tool.category?.name }}
                                    </span>
                                    <Badge v-if="tool.ryan_rating" variant="success" size="sm">
                                        ‚≠ê {{ tool.ryan_rating }}/10
                                    </Badge>
                                </div>
                                <Badge variant="default" size="sm" class="capitalize">
                                    {{ tool.pricing_model }}
                                </Badge>
                            </Card>
                        </Link>
                    </div>

                    <!-- Pagination -->
                    <div v-if="tools.links" class="flex justify-center gap-2">
                        <component
                            v-for="(link, index) in tools.links"
                            :key="index"
                            :is="link.url ? Link : 'span'"
                            :href="link.url || undefined"
                            :class="[
                                'px-4 py-2 rounded-md font-semibold transition-colors',
                                link.active
                                    ? 'bg-primary text-primary-foreground'
                                    : 'bg-card text-foreground',
                                link.url ? 'hover:bg-info hover:text-info-foreground' : 'opacity-50 cursor-not-allowed'
                            ]"
                            v-html="link.label"
                        />
                    </div>
                </div>

                <!-- Empty State -->
                <Card v-else padding="p-12">
                    <div class="text-center">
                        <p class="text-xl text-muted-foreground mb-4">No tools found</p>
                        <Link
                            href="/tools"
                            class="text-info hover:text-info/80 font-semibold"
                        >
                            Clear filters
                        </Link>
                    </div>
                </Card>
            </div>
        </div>
    </GuestLayout>
</template>